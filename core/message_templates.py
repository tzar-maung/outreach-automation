def get_message(name: str = "", 
                category: str = "main",
                niche: str = None) -> str:
    """
    Get a personalized message.
    
    Args:
        name: Target's username/name
        category: Template category (main, short, followup, intro, japanese, jp_short, jp_intro)
        niche: Not used for this template (kept for compatibility)
    
    Returns:
        Personalized message string
    """
    # Clean up name
    name = name.strip().lstrip("@") if name else ""
    
    # Set default name based on language
    if category.startswith("jp") or category == "japanese":
        name = name if name else "„ÅäÂèãÈÅî"
    else:
        name = name if name else "‡∏Ñ‡∏∏‡∏ì"
    
    # Get templates for category
    templates = TEMPLATES.get(category, TEMPLATES["main"])
    
    # Pick random template from category
    template = random.choice(templates)
    
    # Replace variables
    message = template.replace("{name}", name)
    
    return message


def get_template(category: str = "main") -> str:
    """Get a random template from category (without personalization)."""
    templates = TEMPLATES.get(category, TEMPLATES["main"])
    return random.choice(templates)


def get_categories() -> List[str]:
    """Get list of available template categories."""
    return list(TEMPLATES.keys())


def add_template(template: str, category: str = "main"):
    """Add a custom template to a category."""
    if category not in TEMPLATES:
        TEMPLATES[category] = []
    TEMPLATES[category].append(template)


def validate_message(message: str) -> dict:
    """
    Validate a message before sending.
    
    Returns:
        dict with 'valid' (bool) and 'issues' (list)
    """
    issues = []
    
    # Check length (Instagram DM limit is ~1000 chars)
    if len(message) > 1000:
        issues.append(f"Message too long ({len(message)} chars, max 1000)")
    
    # Check for unfilled variables
    if "{name}" in message:
        issues.append("Unfilled {name} variable")
    if "{niche}" in message:
        issues.append("Unfilled {niche} variable")
    
    return {
        "valid": len(issues) == 0,
        "issues": issues,
        "length": len(message),
    }


